-- CREAZIONE TABELLE
DROP TABLE IF EXISTS Composizione CASCADE;
DROP TABLE IF EXISTS Prenotazione CASCADE;
DROP TABLE IF EXISTS Contenuto CASCADE;
DROP TABLE IF EXISTS Fornitura CASCADE;
DROP TABLE IF EXISTS Fornitore CASCADE;
DROP TABLE IF EXISTS Inventario CASCADE;
DROP TABLE IF EXISTS Ricetta CASCADE;
DROP TABLE IF EXISTS Ingrediente CASCADE;
DROP TABLE IF EXISTS Piatto CASCADE;
DROP TABLE IF EXISTS Ordine CASCADE;
DROP TABLE IF EXISTS Cliente CASCADE;
DROP TABLE IF EXISTS Tavolo CASCADE;
DROP TABLE IF EXISTS Sala CASCADE;
DROP TABLE IF EXISTS Dipendente CASCADE;
DROP TABLE IF EXISTS Filiale CASCADE;

CREATE TABLE Filiale (
  id SERIAL PRIMARY KEY,
  indirizzo VARCHAR(255) NOT NULL,
  citta VARCHAR(100) NOT NULL,
  telefono VARCHAR(20) NOT NULL
);

CREATE TABLE Dipendente (
  cf CHAR(16) PRIMARY KEY,
  nome VARCHAR(50) NOT NULL,
  cognome VARCHAR(50) NOT NULL,
  mansione VARCHAR(20) NOT NULL CHECK (mansione IN ('manager','caposala','cuoco','cameriere')),
  stipendio NUMERIC(10,2) NOT NULL,
  filiale INT NOT NULL,
  FOREIGN KEY (filiale) REFERENCES Filiale(id)
);

CREATE TABLE Sala (
  caposala CHAR(16) NOT NULL,
  nome VARCHAR(50) NOT NULL,
  PRIMARY KEY (caposala, nome),
  FOREIGN KEY (caposala) REFERENCES Dipendente(cf)
);

CREATE TABLE Tavolo (
  caposala CHAR(16) NOT NULL,
  sala VARCHAR(50) NOT NULL,
  numero INT NOT NULL,
  num_posti INT NOT NULL,
  PRIMARY KEY (caposala, sala, numero),
  FOREIGN KEY (caposala, sala) REFERENCES Sala(caposala, nome)
);

CREATE TABLE Cliente (
  email VARCHAR(100) PRIMARY KEY,
  nome VARCHAR(50) NOT NULL,
  cognome VARCHAR(50) NOT NULL,
  telefono VARCHAR(20) NOT NULL
);

CREATE TABLE Ordine (
  id BIGSERIAL PRIMARY KEY,
  data_ordinazione DATE NOT NULL,
  ora_ordinazione TIME NOT NULL,
  indirizzo_consegna VARCHAR(255),
  citta_consegna VARCHAR(100),
  is_delivery BOOLEAN NOT NULL,
  cliente VARCHAR(100) NOT NULL,
  filiale INT NOT NULL,
  FOREIGN KEY (cliente) REFERENCES Cliente(email),
  FOREIGN KEY (filiale) REFERENCES Filiale(id),
  CHECK (
    (is_delivery AND indirizzo_consegna IS NOT NULL AND citta_consegna IS NOT NULL)
    OR
    (NOT is_delivery AND indirizzo_consegna IS NULL AND citta_consegna IS NULL)
  )
);

CREATE TABLE Piatto (
  nome VARCHAR(100) PRIMARY KEY,
  prezzo NUMERIC(6,2) NOT NULL CHECK (prezzo > 0),
  categoria VARCHAR(20) NOT NULL CHECK (categoria IN ('antipasto','primo','secondo','dolce'))
);

CREATE TABLE Ingrediente (
  nome VARCHAR(100) PRIMARY KEY,
  unita_misura VARCHAR(20) NOT NULL
);

CREATE TABLE Ricetta (
  piatto VARCHAR(100) NOT NULL,
  ingrediente VARCHAR(100) NOT NULL,
  quantita INT NOT NULL CHECK (quantita > 0),
  PRIMARY KEY (piatto, ingrediente),
  FOREIGN KEY (piatto) REFERENCES Piatto(nome),
  FOREIGN KEY (ingrediente) REFERENCES Ingrediente(nome)
);

CREATE TABLE Inventario (
  ingrediente VARCHAR(100) NOT NULL,
  filiale INT NOT NULL,
  quantita INT NOT NULL CHECK (quantita >= 0),
  PRIMARY KEY (ingrediente, filiale),
  FOREIGN KEY (ingrediente) REFERENCES Ingrediente(nome),
  FOREIGN KEY (filiale) REFERENCES Filiale(id)
);

CREATE TABLE Fornitore (
  partita_iva CHAR(11) PRIMARY KEY,
  telefono VARCHAR(20) NOT NULL,
  email VARCHAR(100) NOT NULL
);

CREATE TABLE Fornitura (
  id BIGSERIAL PRIMARY KEY,
  manager CHAR(16) NOT NULL,
  fornitore CHAR(11) NOT NULL,
  data DATE NOT NULL,
  ora TIME NOT NULL,
  FOREIGN KEY (manager) REFERENCES Dipendente(cf),
  FOREIGN KEY (fornitore) REFERENCES Fornitore(partita_iva)
);

CREATE TABLE Contenuto (
  fornitura BIGINT NOT NULL,
  ingrediente VARCHAR(100) NOT NULL,
  quantita INT NOT NULL CHECK (quantita > 0),
  prezzo_unita NUMERIC(6,2) NOT NULL CHECK (prezzo_unita > 0),
  PRIMARY KEY (fornitura, ingrediente),
  FOREIGN KEY (fornitura) REFERENCES Fornitura(id),
  FOREIGN KEY (ingrediente) REFERENCES Ingrediente(nome)
);

CREATE TABLE Prenotazione (
  cliente VARCHAR(100) NOT NULL,
  caposala CHAR(16) NOT NULL,
  sala VARCHAR(50) NOT NULL,
  tavolo INT NOT NULL,
  data_prenotazione DATE NOT NULL,
  ora_inizio TIME NOT NULL,
  ora_fine TIME,
  num_persone INT NOT NULL CHECK (num_persone > 0),
  PRIMARY KEY (cliente, caposala, sala, tavolo, data_prenotazione, ora_inizio),
  FOREIGN KEY (cliente) REFERENCES Cliente(email),
  FOREIGN KEY (caposala, sala, tavolo) REFERENCES Tavolo(caposala, sala, numero)
);

CREATE TABLE Composizione (
  ordine BIGINT NOT NULL,
  piatto VARCHAR(100) NOT NULL,
  quantita INT NOT NULL CHECK (quantita > 0),
  PRIMARY KEY (ordine, piatto),
  FOREIGN KEY (ordine) REFERENCES Ordine(id),
  FOREIGN KEY (piatto) REFERENCES Piatto(nome)
);


